class ConstructionDataset(utils.Dataset):

    def load_images(self, dataset_dir, subset):
        """Load a subset of the construction dataset.
        dataset_dir: Root directory of the dataset.
        subset: Subset to load: train or val
        """
        # Add classes. Note that we inherit the "BG" (background)
        # class from Dataset. For now, we train our model only on polygons
        self.add_class("construction", 1, "Vertical_formwork")
        self.add_class("construction", 2, "Concrete_pump_hose")
        # self.add_class("construction", 3, "People")
        # self.add_class("construction", 4, "Mixer_truck")

        # Train or validation dataset?
        assert subset in ["Train", "Valid"]
        labels_dir = os.path.join(dataset_dir, "labels_json", subset)
        images_dir = os.path.join(dataset_dir, "images", subset)

        # Load annotations
        # We mostly care about the x and y coordinates of each region
        label_paths = sorted(glob.glob(os.path.join(labels_dir, "*json")))
        annotations = load_labels_from_folder(label_paths)
        i = 0
        for a in annotations:
          objects = a["objects"]
          shapes = objects["shape_attributes"]
          height, width = a['size']["height"], a['size']["width"]
          img_filename = ".".join(a['filename'].split(".")[:-1])
          image_path = os.path.join(images_dir, img_filename)
          self.add_image(source="construction",
                         image_id=i,
                         path=image_path,
                         width=width, height=height,
                         shapes=shapes)
          i +=1

    def load_mask(self, image_id):
        """Generate instance masks for an image.
       Returns:
        masks: A bool array of shape [height, width, instance count] with
            one mask per instance.
        class_ids: a 1D array of class IDs of the instance masks.
        """
        info = filter_on_image_id(self.image_info, image_id)
        mask = np.zeros([info["height"], info["width"], len(info["shapes"])],
                        dtype=np.uint8)
        for i, p in enumerate(info["shapes"]):
          # Get indexes of pixels inside the polygon and set them to 1
          rr, cc = skimage.draw.polygon(p['all_points_y'], p['all_points_x'])
          mask[rr, cc, i] = 1
        return mask.astype(np.bool), np.ones([mask.shape[-1]], dtype=np.int32)

    def image_reference(self, image_id):
        """Return the path of the image."""
        info = self.image_info[image_id]
        if info["source"] == "balloon":
            return info["path"]
        else:
            super(self.__class__, self).image_reference(image_id)


